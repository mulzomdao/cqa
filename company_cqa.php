                            이제는 국내최대규모의 퀼트전문페스티벌로 자리매김을 하였습니다. 
                            국내외의 우수한 퀼트 작품 전시를 통하여 한국퀼트의 대중화 및 발전을 꾀하고, 
                            매년 새로운 주제로 공모전을 열어, 새로운 작가를 발굴하고, 관련 산업의 진흥을 도모하며, 
                            다양한 접근을 통해 퀼트인 뿐 아니라 일반인들과의 소통 및 저변확대에 힘쓰고 있습니다.
                        </p>     
                        <p>
                            전통퀼트와 아트퀼트, 테마퀼트, 실용퀼트 등의 분야에서 다양한 퀼트 작품들을 만나볼 수 있으며, 
                            국내외 초대작가전, 외국 퀼터들과의 챌린지퀼트전, 퀼트가방 기획전, 나의 아뜰리에전, 퀼트그룹전, 
