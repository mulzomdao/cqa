                            다양한 워크숍, 재활용전, 자선경매 등 퀼터로서의 교육 및 사회 참여의식 고취에도 힘쓰고 있습니다. 
                            또한 2015년 제주, 전주를 시작으로, 2016년 광주, 2017년에는 군산, 전주 등 지방전시를 개최하고 있습니다.
