                            자격요건의 기준을 마련하여 새로운 퀼트의 연구개발을 위한 커리큘럼과 자격증발급을 위해 꾸준히 노력하고 있습니다.
                        </p>     
                        <p>
                            2008년 8월, 문화관광부(현 문화체육관광부)에 의해 사단법인으로 승인받은 CQA는 
                            2018년 현재 전국에 여섯 지회, 열개의 지부를 포함하는 지역사업부, 
                            아트퀼트분과와 실용전통공예분과로 구성된 A&C(Art & Craft)부, 운영부, 
                            그리고 QFIK 준비위원회로 구성되어 있으며, 현재 1000여명의 회원과 함께하고 있습니다.
                        </p>     
                        <p>
                            <Strong>
                                사단법인 한국퀼트연합은 핸드퀼트(2급/강사)와 머신퀼트(2급/강사) 자격증을 발급하고 있으며, 
                                교육과학기술부에 민간자격으로 등록되어 있습니다. 
                            </Strong>
                        </p>     
                        <p>
                            핸드퀼트와 머신퀼트로 나누어 매년 2급자격 검정시험(3~4월)을 통과하고, 2급교육을 마친 회원에게 2급자격증을 발급하고 있으며, 
